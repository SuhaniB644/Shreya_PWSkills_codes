/*Design a calculator to perform basic arithmetic operations (+,-,/,*)*/
#include<iostream>
using namespace std;
int main(){
    float n1, n2;
    cout<<"Enter two numbers: "<<endl;
    cin>>n1>>n2;

    cout<<"Sum: "<<n1 + n2<<endl;
    cout<<"Difference: "<<n1 - n2<<endl;
    cout<<"Product: "<<n1 * n2<<endl;
    cout<<"Quotient: "<<n1 / n2<<endl;
    
}